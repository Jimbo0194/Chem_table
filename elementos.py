class elemento:

    def __init__(self, nombre, tipo, siglas, pesoAtomico, clasificacion, valencia, oxidacion, posicion):
        self.nombre = nombre
        self.tipo = tipo
        self.siglas = siglas
        self.pesoAtomico = pesoAtomico
        self.clasificacion = clasificacion
        self.valencia = valencia
        self.oxidacion = oxidacion
        self.posicion = posicion
